    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String txt1 = txtPorcentagem.getText();
        String txt2 = txtResult.getText();
        
        int dialogButton = JOptionPane.showConfirmDialog(null, "VocÃª tem certeza?");
        
        
        
        if(dialogButton == 0){
        txtResult.setText("");
        txtPorcentagem.setText("");
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);
        jRadioButton3.setSelected(false);
        }
        else if (dialogButton == 1){
            
        txtPorcentagem.setText(""+txt1);
        txtResult.setText(""+txt2);
        }
        else if (dialogButton == 2){
        
        txtPorcentagem.setText(""+txt1);
        txtResult.setText(""+txt2);
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

